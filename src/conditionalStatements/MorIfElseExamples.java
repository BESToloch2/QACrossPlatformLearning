package conditionalStatements;

public class MorIfElseExamples {
    public static void main(String[] args) {
        boolean hungry=true;

        if(hungry){
            System.out.println("I am going to eat");
        }

        boolean lightON=true;
        if(lightON){
            System.out.println("Room is bright");
        }else{
            System.out.println("Room is dark");
        }

    }
}
